
public class rgbDobles {
	double R;
	double G;
	double B;
	
	public rgbDobles(double r, double g, double b) {
		super();
		R = r;
		G = g;
		B = b;
	}
	public rgbDobles() {
		super();
		R = 0.0;
		G = 0.0;
		B = 0.0;
	}
	
	public double getR() {
		return R;
	}
	public void setR(double r) {
		R = r;
	}
	public double getG() {
		return G;
	}
	public void setG(double g) {
		G = g;
	}
	public double getB() {
		return B;
	}
	public void setB(double b) {
		B = b;
	}

	
	

}
